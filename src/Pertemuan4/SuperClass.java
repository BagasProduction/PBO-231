package Pertemuan4;

public class SuperClass {
    private String nama, keterangan;

    public SuperClass(String nama, String keterangan) {
        this.nama = nama;
        this.keterangan = keterangan;
    }

    public String getNama() {
        return nama;
    }

    public String getKeterangan() {
        return keterangan;
    }
}
